export const getClassColor = (className) => {
  switch (className) {
    case 'Death Knight':
      return '#C41F3B'
    case 'Demon Hunter':
      return '#A330C9'
    case 'Druid':
      return '#FF7D0A'
    case 'Hunter':
      return '#ABD473'
    case 'Mage':
      return '#40C7EB'
    case 'Monk':
      return '#00FF96'
    case 'Paladin':
      return '#F58CBA'
    case 'Priest':
      return '#FFFFFF'
    case 'Rogue':
      return '#FFF569'
    case 'Shaman':
      return '#0070DE'
    case 'Warlock':
      return '#8787ED'
    case 'Warrior':
      return '#C79C6E'
  }
}
